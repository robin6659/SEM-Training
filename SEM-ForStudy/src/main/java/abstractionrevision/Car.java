package abstractionrevision;

public abstract class Car {
	
	public abstract void sound();
	
}

/*"Abstraction in Java is achieved using abstract classes and interfaces. 
 * It allows us to focus on what an object does rather than how it does it. 
 * when I use a Car class,
 * I can call start() or drive() without knowing exactly how the engine works. 
 * This makes the code cleaner, more flexible, and easier to maintain."
 */